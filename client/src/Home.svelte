        <p>There is a stand deck of 52 cards in game.</p>
        <p>At the start of the game each players gets 1 random card.</p>
        <p>
            One it is Your turn during the round, there are 2 possible moves: Raise and Check.
            <br />
            Raise - You can choose a Hand Ranking, for example Pair of Aces. That means that you bet there are 2 Aces among ALL dealt cards in current round.
            <br />
            Check - You can check previous player's bet. > If his bet was correct then You will be the one to receive another card. > If his bet was incorrect then previous player will
            receive additional card and another he will begin next round. For example if previous player bets there is One King and You check it and there are no dealt kings in current
            round -> Previous Player loses that round.
        </p>
        <p>Player is eliminated once he loses a round with 5 cards.</p>
        <p>There can be maximum 5 players in 1 game.</p>
